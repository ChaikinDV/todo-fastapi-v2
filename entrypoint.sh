#!/bin/bash
# Этот скрипт запускается ПЕРЕД основным приложением
# Он ждёт, пока БД поднимется, и накатывает миграции

set -e  # Останавливаем скрипт при любой ошибке

echo "=== Ждём готовности базы данных ==="
# Проверяем доступность PostgreSQL через порт 5432
# Используем netcat (nc) - утилиту для проверки сети
# -z : проверить порт, не отправляя данные
# -v : подробный вывод
while ! nc -z db 5432; do
  echo "База данных ещё не готова, ждём 1 секунду..."
  sleep 1
done
echo "=== База данных готова! ==="

echo "=== Применяем миграции Alembic ==="
# Запускаем миграции (обновляем схему БД до последней версии)
alembic upgrade head

echo "=== Запускаем приложение FastAPI ==="
# Запускаем сервер
exec uvicorn app.main:app --host 0.0.0.0 --port 8000 --reload
